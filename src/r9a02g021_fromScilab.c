#include "r_smc_entry.h"

//includes a√±adidos
#include "r_cg_interrupt_handlers.h"
#include "r_cg_uarta_common.h"
#include "r_cg_uarta.h"
#include "r_smc_interrupt.h"
#include "r_cg_icu.h"
#include "Pin.h"
#include "_frenoEmergencia.h"



MD_STATUS g_uarta1_tx_end = 0U;     /* uartA1 transmission end */
volatile const uint8_t message[10] = {"FRENO OK "};    /* Mensaje de freno ok */

volatile uint32_t blinkDelay = 500; // Initial blink delay of 0.5 second
extern volatile int boton;

volatile double speed = 0;
double emergencyBreak;
volatile int blink = 0;

int main(void);

int main(void)
{
    /* Start SW Interrupt */
    R_Config_ICU_IRQ4_Start();

    /* Start UART */
    R_Config_UARTA1_Start();

    while(1)
    {
    	if (boton == 1){
    		speed = 15;  //dentro del rango mayor 10 y menor a 20 ENTONCES PARPADEO LED
    		blink = 1;
    	}
    	else if (boton == 0){
    		speed = 30; //fuera del rango mayor 10 y menor a 20 ENTONCES ENVIA MENSAJE FRENO OK
    		blink = 0;
    	}

    	_frenoEmergencia(&emergencyBreak, speed);

    	if((emergencyBreak != 0.0) && (blink == 1)){ //dentro del rango
    		PIN_WRITE(LED1) = ~PIN_READ(LED1); //Parpadeo LED
    		R_BSP_SoftwareDelay(blinkDelay, BSP_DELAY_MILLISECS);
    	}
    	else{
    		PIN_WRITE(LED1) = 0;
    		R_BSP_SoftwareDelay(1000, BSP_DELAY_MILLISECS); // DELAY 1s
    		g_uarta1_tx_end = R_Config_UARTA1_Send((uint8_t *)message, sizeof(message));
    	}
    }
    R_Config_ICU_IRQ4_Stop();
    return 0;
}





